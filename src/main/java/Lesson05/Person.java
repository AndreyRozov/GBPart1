package Lesson05;

public class Person {
    String name;
    String position;
    String email;
    int phone_number;
    int salary;
    int age;

    public Person(String name, String position, String email, int phone_number, int salary, int age) {
        this.name = name;
        this.position = position;
        this.email = email;
        this.phone_number = phone_number;
        this.salary = salary;
        this.age = age;
    }

}
